# Coordinates (row,col) of a 3x3 box are as follow:
#
# -1,0  | -1,1  | -1,2
# ––––––––––––––––––––
#  0,0  |  0,1  |  0,2
# ––––––––––––––––––––
#  1,0  |  1,1  |  1,2
#
class PiecesStates
  def self.get(name, angle)
    self.send(name)[angle]
  end

  def self.i
    {
      0 => [[0, 0], [0, 1], [0, 2], [0, 3]],
      90 => [[-1, 1], [0, 1], [1, 1], [2, 1]],
      180 => [[1, 0], [1, 1], [1, 2], [1, 3]],
      270 => [[-1, 2], [0, 2], [1, 2], [2, 2]],
    }
  end

  def self.s
    {
      0 => [[0, 0], [0, 1], [-1, 1], [-1, 2]],
      90 => [[0, 0], [0, 1], [-1, 0], [1, 1]],
      180 => [[0, 1], [0, 2], [1, 0], [1, 1]],
      270 => [[-1, 1], [0, 1], [0, 2], [1, 2]],
    }
  end

  def self.z
    {
      0 => [[0, 1], [0, 2], [-1, 0], [-1, 1]],
      90 => [[0, 0], [0, 1], [-1, 1], [1, 0]],
      180 => [[0, 0], [0, 1], [1, 1], [1, 2]],
      270 => [[-1, 2], [0, 1], [0, 2], [1, 1]],
    }
  end

  def self.t
    {
      0 => [[0, 0], [0, 1], [0, 2], [-1, 1]],
      90 => [[0, 0], [0, 1], [-1, 1], [1, 1]],
      180 => [[0, 0], [0, 1], [0, 2], [1, 1]],
      270 => [[0, 2], [0, 1], [-1, 1], [1, 1]],
    }
  end

  def self.l
    {
      0 => [[0, 0], [0, 1], [0, 2], [-1, 2]],
      90 => [[-1, 0], [-1, 1], [0, 1], [1, 1]],
      180 => [[0, 0], [0, 1], [0, 2], [1, 0]],
      270 => [[-1, 1], [0, 1], [1, 1], [1, 2]],
    }
  end

  def self.j
    {
      0 => [[0, 0], [0, 1], [0, 2], [-1, 0]],
      90 => [[-1, 1], [0, 1], [1, 1], [1, 0]],
      180 => [[0, 0], [0, 1], [0, 2], [1, 2]],
      270 => [[-1, 1], [-1, 2], [0, 1], [1, 1]],
    }
  end

  def self.o
    # Just 0 should be enough because this piece can only have an angle
    # of 0°.
    {
      0 => [[0, 0], [0, 1], [-1, 0], [-1, 1]],
      90 => [[0, 0], [0, 1], [-1, 0], [-1, 1]],
      180 => [[0, 0], [0, 1], [-1, 0], [-1, 1]],
      270 => [[0, 0], [0, 1], [-1, 0], [-1, 1]],
    }
  end
end
